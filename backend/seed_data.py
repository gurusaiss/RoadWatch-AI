def seed():
    create_tables()
    db = SessionLocal()
    try:
        if db.query(Road).count() > 0:
            print("DB already seeded. Skipping.")
            return

        print("Seeding comprehensive database...")
        road_objs = [Road(**r) for r in ROADS]
        db.add_all(road_objs)
        db.flush()

        # Budget history
        road_id_map = {r.road_id: r.id for r in road_objs}
        # Map index-based budget references to actual IDs
        road_idx_map = {i+1: r.id for i, r in enumerate(road_objs)}
        for (idx, fy, wt, san, sp, cur, cont, ws, we, su, sl) in BUDGET_HISTORY:
            rid = road_idx_map.get(idx)
            if rid:
                db.add(BudgetRecord(road_id=rid, financial_year=fy, work_type=wt,
                    amount_sanctioned=san, amount_spent=sp, currency=cur,
                    contractor=cont, work_start=ws, work_end=we,
                    source_url=su, source_label=sl))

        db.add_all([Authority(**a) for a in AUTHORITIES])

        for c in COMPLAINTS:
            db.add(Complaint(**c))

        db.commit()
        print(f"Seeded: {len(ROADS)} roads | {len(AUTHORITIES)} authorities | {len(COMPLAINTS)} complaints")
    except Exception as e:
        db.rollback()
        print(f"Seed error: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    seed()
